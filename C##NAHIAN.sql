CREATE TRIGGER "C##NAHIAN"."ORDER_LOCATION_NOTIFICATION_TRIGGER" AFTER INSERT OR UPDATE OF "CURRENT_SUB_DISTRICT" ON "C##NAHIAN"."ORDER" REFERENCING OLD AS "OLD" NEW AS "NEW" FOR EACH ROW 
DECLARE
    N_USER_NAME VARCHAR2(20);
    N_TEXT VARCHAR2(200);
    N_SEEN NUMBER;
    N_TIME NUMBER;
    N_TYPE VARCHAR(20);
    CUR_SUB_DISTRICT_NAME VARCHAR2(20);
    CUR_DISTRICT_NAME VARCHAR2(20);
BEGIN
    IF :NEW.CURRENT_SUB_DISTRICT IS NULL THEN
       RETURN;
    END IF;
    SELECT SUB_DISTRICT_NAME INTO CUR_SUB_DISTRICT_NAME
    FROM SUB_DISTRICT WHERE SUB_DISTRICT_ID= :NEW.CURRENT_SUB_DISTRICT;

    SELECT DISTRICT_NAME INTO CUR_DISTRICT_NAME
    FROM SUB_DISTRICT JOIN DISTRICT
    ON SUB_DISTRICT.DISTRICT_ID = DISTRICT.DISTRICT_ID
    WHERE SUB_DISTRICT_ID=:NEW.CURRENT_SUB_DISTRICT;

    N_TEXT:='Your Order is currently at: '||CUR_SUB_DISTRICT_NAME||','||CUR_DISTRICT_NAME||'.';

    N_USER_NAME:=:NEW.CLIENT_NAME;
    N_SEEN:=0;
    N_TIME:=extract(day from(sys_extract_utc(systimestamp) - to_timestamp('1970-01-01', 'YYYY-MM-DD'))) * 86400000
            + to_number(to_char(sys_extract_utc(systimestamp), 'SSSSSFF3'));
    N_TYPE:='ORDER';
    INSERT INTO NOTIFICATION(USER_NAME, TEXT, SEEN, TIME, TYPE)
    VALUES(N_USER_NAME,N_TEXT,N_SEEN,N_TIME,N_TYPE);
END;
