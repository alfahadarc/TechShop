CREATE TRIGGER "C##NAHIAN"."COMMENT_ACHIEVEMENT_TRIGGER" BEFORE INSERT ON "C##NAHIAN"."COMMENT" REFERENCING OLD AS "OLD" NEW AS "NEW" FOR EACH ROW 
DECLARE
    COMMENT_COUNT NUMBER;
    ACHIEVEMENT_EXIST NUMBER;
    CUR_ACQUIRED_STEPS NUMBER;
    CUR_TOTAL_STEPS NUMBER;
BEGIN
    IF :NEW.PARENT_COMMENT IS NOT NULL THEN
        RETURN ;
    end if;
    SELECT COUNT(*) INTO COMMENT_COUNT
    FROM "COMMENT" C
    WHERE C.COMMENTOR=:NEW.COMMENTOR AND C.PARENT_COMMENT IS NULL;
    SELECT COUNT(*) INTO ACHIEVEMENT_EXIST
        FROM CLIENT_ACHIEVEMENT CA
         WHERE CA.TITLE='Social Butterfly' AND CA.CLIENT_NAME=:NEW.COMMENTOR;
    IF COMMENT_COUNT=0 OR ACHIEVEMENT_EXIST=0 THEN
        SELECT COUNT(*) INTO ACHIEVEMENT_EXIST
        FROM CLIENT_ACHIEVEMENT CA
        WHERE CA.TITLE='Social Butterfly' AND CA.CLIENT_NAME=:NEW.COMMENTOR;
        IF ACHIEVEMENT_EXIST=0 THEN
            INSERT INTO CLIENT_ACHIEVEMENT(TITLE, CLIENT_NAME,ACQUIRED_STEPS)
            VALUES ('Social Butterfly',:NEW.COMMENTOR,1);
        ELSE
            SELECT ACQUIRED_STEPS INTO CUR_ACQUIRED_STEPS FROM CLIENT_ACHIEVEMENT
                WHERE TITLE='Social Butterfly' AND CLIENT_NAME=:NEW.COMMENTOR;
            SELECT TOTAL_STEPS INTO CUR_TOTAL_STEPS FROM ACHIEVEMENT
                WHERE TITLE='Social Butterfly';
            IF CUR_ACQUIRED_STEPS<CUR_TOTAL_STEPS THEN
                UPDATE CLIENT_ACHIEVEMENT
                    SET ACQUIRED_STEPS=(ACQUIRED_STEPS+1)
                    WHERE TITLE='Social Butterfly' AND CLIENT_NAME=:NEW.COMMENTOR;
            end if;
        end if;
    end if;
END;
