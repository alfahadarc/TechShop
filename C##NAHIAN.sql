CREATE TRIGGER "C##NAHIAN"."COMMENT_NOTIFICATION_TRIGGER" BEFORE INSERT ON "C##NAHIAN"."COMMENT" REFERENCING OLD AS "OLD" NEW AS "NEW" FOR EACH ROW 
DECLARE
    PARENT_COMMENTOR VARCHAR2(20);
    COMMENT_TEXT VARCHAR2(120);
    N_USER_NAME VARCHAR2(20);
    N_TEXT VARCHAR2(200);
    N_SEEN NUMBER;
    N_TIME NUMBER;
    N_TYPE VARCHAR(20);
    N_ID NUMBER;
BEGIN
    IF :NEW.PARENT_COMMENT IS NULL THEN
        return;
    END IF;
    SELECT C1.COMMENTOR INTO PARENT_COMMENTOR
    FROM "COMMENT" C1 WHERE C1.COMMENT_ID= :NEW.PARENT_COMMENT;
    IF PARENT_COMMENTOR= :NEW.COMMENTOR THEN
        return ;
    END IF;
    N_USER_NAME:=PARENT_COMMENTOR;
    N_SEEN:=0;
    N_TIME:=extract(day from(sys_extract_utc(systimestamp) - to_timestamp('1970-01-01', 'YYYY-MM-DD'))) * 86400000
            + to_number(to_char(sys_extract_utc(systimestamp), 'SSSSSFF3'));
    N_TYPE:='COMMENT';
    IF (LENGTH(:NEW.TEXT)>110) THEN
        COMMENT_TEXT:=(SUBSTR(:NEW.TEXT,1,110)||'...');
    ELSE
        COMMENT_TEXT:=:NEW.TEXT;
    end if;
    N_TEXT:=(:NEW.COMMENTOR||' replied to your comment: "'||COMMENT_TEXT||'"');
    INSERT INTO NOTIFICATION(USER_NAME, TEXT, SEEN, TIME, TYPE)
    VALUES(N_USER_NAME,N_TEXT,N_SEEN,N_TIME,N_TYPE)
    RETURNING NOTIFICATION_ID INTO N_ID;

    INSERT INTO COMMENT_NOTIFY(NOTIFICATION_ID, PRODUCT_ID)
    VALUES (N_ID, :NEW.COMMENT_PRODUCT_ID);
END;
